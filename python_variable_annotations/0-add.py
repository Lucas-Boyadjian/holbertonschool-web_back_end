#!/usr/bin/env python3

"""
This module provides a function to add two floating
point numbers with type annotations.
"""


def add(a: float, b: float) -> float:
    """
    Additionne deux nombres flottants.
    Args:
        a (float): premier nombre
        b (float): second nombre
    Returns:
        float: la somme de a et b

    """
    return a + b
